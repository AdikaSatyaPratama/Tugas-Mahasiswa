    private void jButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahActionPerformed
        // TODO add your handling code here:
    String nidn = jTextFieldNidn.getText().trim();
    String name = jTextFieldName.getText().trim();
    String expertise = jTextFieldExpertise.getText().trim();
    
    if (nidn.isEmpty() || name.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Nidn dan Nama tidak boleh kosong!");
        return;
    }
    model.Lecturer lecNew = new model.Lecturer(0, nidn, name, expertise);
 
    if(lecController.create(lecNew) > 0){
        javax.swing.JOptionPane.showMessageDialog(this, "Data Dosen berhasil ditambah");
        loadData("");
        
        jTextFieldNidn.setText("");
        jTextFieldName.setText("");
        jTextFieldExpertise.setText("");
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menambah data.");
    }
    }//GEN-LAST:event_jButtonTambahActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
    String nidn = jTextFieldNidn.getText().trim();
    String name = jTextFieldName.getText().trim();
    String expertise = jTextFieldExpertise.getText().trim();

    if (idLecturerTerpilih == 0 || nidn.isEmpty() || name.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih data pada tabel terlebih dahulu!");
        return;
        }
    model.Lecturer lecEdit = new model.Lecturer(idLecturerTerpilih, nidn, name, expertise);
    
    if (lecController.update(lecEdit) > 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Data Dosen Berhasil Diubah!");
        loadData(""); // Refresh tabel
        
        idLecturerTerpilih = 0;
        jTextFieldNidn.setEditable(true); 
        jTextFieldNidn.setText("");
        jTextFieldName.setText("");
        jTextFieldExpertise.setText("");  
    }else {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengubah data.");
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
        // TODO add your handling code here:
    String nidn = jTextFieldNidn.getText().trim();

    if (nidn.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih data pada tabel yang ingin dihapus!");
        return;
    }
    
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus Nidn " + nidn + "?", "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm == javax.swing.JOptionPane.YES_OPTION){
        if (lecController.delete(nidn) > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data Dosen Berhasil Dihapus!");
            loadData(""); 
            jTextFieldNidn.setEditable(true);
            jTextFieldNidn.setText("");
            jTextFieldName.setText("");
            jTextFieldExpertise.setText("");     
        }
    }else {
        javax.swing.JOptionPane.showMessageDialog(this, "Data gagal dihapus!");
    }
    }//GEN-LAST:event_jButtonHapusActionPerformed

