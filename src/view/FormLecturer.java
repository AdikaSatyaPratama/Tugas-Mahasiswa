public void loadData(String keyword){
String[] column = {"ID", "Nidn" , "Nama", "Expertise"};
javax.swing.table.DefaultTableModel modelTabel = new javax.swing.table.DefaultTableModel(column, 0);

java.util.List<model.Lecturer> listlec = lecController.getLecturer(keyword, halamanSekarang, jumlahPerHalaman);

for (model.Lecturer lec : listlec){
        Object[] row = {lec.getId(), lec.getNidn(), lec.getName(), lec.getExpertise()};
        modelTabel.addRow(row);
    }
    jTableLecturer.setModel(modelTabel);
    
    jLabelHalaman.setText("Halaman " + halamanSekarang);
}
