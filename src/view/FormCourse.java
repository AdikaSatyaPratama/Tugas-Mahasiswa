    private void jButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahActionPerformed
        // TODO add your handling code here:
    String code = jTextFieldCode.getText().trim();
    String name = jTextFieldName.getText().trim();
    int sks = Integer.parseInt(jComboBoxSks.getSelectedItem().toString());
    
    if (code.isEmpty() || name.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Code dan Nama Matakuliah tidak boleh kosong!");
        return;
    }
    model.Course courseNew = new model.Course(0, code, name, sks );
 
    if(courseController.create(courseNew) > 0){
        javax.swing.JOptionPane.showMessageDialog(this, "Data Matakuliah berhasil ditambah");
        loadData("");
        
        jTextFieldCode.setText("");
        jTextFieldName.setText("");
        jComboBoxSks.setSelectedIndex(0);
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menambah data.");
        }
        
    }//GEN-LAST:event_jButtonTambahActionPerformed

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
String code = jTextFieldCode.getText().trim();
String name = jTextFieldName.getText().trim();
int sks = Integer.parseInt(jComboBoxSks.getSelectedItem().toString());

if (idCourseTerpilih == 0 || code.isEmpty() || name.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Pilih data pada tabel terlebih dahulu!");
    return;
}

model.Course courseEdit = new model.Course(idCourseTerpilih, code, name, sks);
if (courseController.update(courseEdit) > 0) {
    javax.swing.JOptionPane.showMessageDialog(this, "Data matakuliah berhasil diubah!");
    loadData("");
    idCourseTerpilih = 0;
    jTextFieldCode.setText("");
    jTextFieldName.setText("");
    jComboBoxSks.setSelectedIndex(0);
} else {
    javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengubah data.");
}
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
        // TODO add your handling code here:
    if (idCourseTerpilih == 0) {
    javax.swing.JOptionPane.showMessageDialog(this, "Pilih data pada tabel terlebih dahulu!");
    return;
}

int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Hapus matakuliah ini?", "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);
if (confirm == javax.swing.JOptionPane.YES_OPTION) {
    if (courseController.delete(idCourseTerpilih) > 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Data matakuliah berhasil dihapus!");
        loadData("");
        idCourseTerpilih = 0;
        jTextFieldCode.setText("");
        jTextFieldName.setText("");
        jComboBoxSks.setSelectedIndex(0);
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus data.");
    }
}
    }//GEN-LAST:event_jButtonHapusActionPerformed

    private void jTableCourseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCourseMouseClicked
        // TODO add your handling code here:
    int row = jTableCourse.getSelectedRow();
    if (row != -1) {
    idCourseTerpilih = Integer.parseInt(jTableCourse.getValueAt(row, 0).toString());
    jTextFieldCode.setText(jTableCourse.getValueAt(row, 1).toString());
    jTextFieldName.setText(jTableCourse.getValueAt(row, 2).toString());
    jComboBoxSks.setSelectedItem(jTableCourse.getValueAt(row, 3).toString());
}
    }//GEN-LAST:event_jTableCourseMouseClicked

