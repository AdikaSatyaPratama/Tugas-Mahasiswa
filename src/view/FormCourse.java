 private void loadData(String keyword){
        String[] column = {"", "Code", "Matakuliah", "Sks"};
        javax.swing.table.DefaultTableModel modelTabel = new javax.swing.table.DefaultTableModel(column, 0);
        
        java.util.List<model.Course> listCourse = courseController.getCourse(keyword, halamanSekarang, jumlahPerHalaman);
        
        for (model.Course course : listCourse){
            Object[] row = {course.getId(), course.getCode(), course.getName(), course.getSks()};
            modelTabel.addRow(row);
        }
        jTableCourse.setModel(modelTabel);
        jLabelHalaman.setText("Halaman " + halamanSekarang);
    }
    

