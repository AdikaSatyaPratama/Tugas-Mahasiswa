    private void jButtonPreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPreviousActionPerformed
        // TODO add your handling code here:
        if (halamanSekarang > 1) {
        halamanSekarang--;
        loadData("");     
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Anda sudah berada di halaman pertama!");
    }
    }//GEN-LAST:event_jButtonPreviousActionPerformed

    private void jButtonNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextActionPerformed

    int jumlahBarisTabel = jTableKrs.getRowCount();
    
    if (jumlahBarisTabel < jumlahPerHalaman) {
        javax.swing.JOptionPane.showMessageDialog(this, "Anda sudah berada di halaman terakhir!");
        return;
    }
    
    // 2. Jika baris tabel masih penuh (sama dengan limit), ada kemungkinan halaman berikutnya masih ada data
    halamanSekarang++;
    loadData("");     
    
    
    if (jTableKrs.getRowCount() == 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Tidak ada data lagi di halaman berikutnya.");
        halamanSekarang--; 
        loadData("");      
    }
    }//GEN-LAST:event_jButtonNextActionPerformed

