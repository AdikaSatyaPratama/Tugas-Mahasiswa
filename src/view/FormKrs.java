    private void isiComboBox() {
        jComboBoxMahasiswa.removeAllItems();
        jComboBoxCourse.removeAllItems();
        jComboBoxLecturer.removeAllItems();
        
        java.util.List<model.Mahasiswa> listMhs = mahasiswaController.getMahasiswa("", 1, 100);
        for (model.Mahasiswa mhs : listMhs) {
            // Tampilan visual: "NIM - Nama"
            jComboBoxMahasiswa.addItem(mhs.getNim() + " - " + mhs.getNamaMahasiswa());
        }
        
        java.util.List<model.Course> listCourse = courseController.getCourse("", 1, 100); // sesuaikan method get-mu
        for (model.Course c : listCourse) {
            // Tampilan visual: "Nama Matakuliah"
            jComboBoxCourse.addItem(c.getName());
        }
        
        java.util.List<model.Lecturer> listLecturer = lecturerController.getLecturer("", 1, 100);
        for (model.Lecturer lec : listLecturer) {
            jComboBoxLecturer.addItem(lec.getName());
        }
    }
 
    private void loadData(String keyword) {
        String[] column = { "ID", "NIM", "Nama Mahasiswa", "Mata Kuliah", "Dosen Pengajar", "Semester", "Grade"};
        javax.swing.table.DefaultTableModel modelTabel = new javax.swing.table.DefaultTableModel(column, 0);
        
        java.util.List<model.Krs> listKrs = krsController.getKrs(keyword, halamanSekarang, jumlahPerHalaman);
        
        for (model.Krs krs : listKrs) {
        String gradeHuruf = hitungGradeHuruf(krs.getNilaiSikap(), krs.getNilaiUts(), krs.getNilaiUas());

        Object[] baris = {
            krs.getIdKrs(),       
            krs.getNimMahasiswa(), 
            krs.getNamaMahasiswa(),  
            krs.getNamaCourse(),     
            krs.getNamaLecturer(),  
            krs.getSemester(),      
            gradeHuruf               
        };
        modelTabel.addRow(baris);
    }
        
        jTableKrs.setModel(modelTabel); 
        jTableKrs.getColumnModel().getColumn(0).setMinWidth(0);
        jTableKrs.getColumnModel().getColumn(0).setMaxWidth(0);
        jTableKrs.getColumnModel().getColumn(0).setPreferredWidth(0);
    
        jLabelHalaman.setText("Halaman " + halamanSekarang);
    }

