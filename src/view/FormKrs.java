        int idKrsTerpilih = Integer.parseInt(jTableKrs.getValueAt(barisTerpilih, 0).toString());
        
        
        if (krsController.delete(idKrsTerpilih) > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data KRS berhasil dihapus!");
            loadData(""); 
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus data KRS.");
        }
