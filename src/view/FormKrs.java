    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int indeksMhs = jComboBoxMahasiswa.getSelectedIndex();
        int indeksCourse = jComboBoxCourse.getSelectedIndex();
        int indeksLecturer = jComboBoxLecturer.getSelectedIndex();
    
    if (indeksMhs == -1 || indeksCourse == -1 || indeksLecturer == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pastikan semua data master sudah dipilih!");
        return;
    }
    
    if (jTextFieldSikap.getText().trim().isEmpty() || jTextFieldUts.getText().trim().isEmpty() || jTextFieldUas.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Nilai Sikap, UTS, dan UAS wajib diisi angka (isi 0 jika belum ada nilai)!");
        return;
    }
    
    try {
        double sikap = Double.parseDouble(jTextFieldSikap.getText().trim());
        double uts = Double.parseDouble(jTextFieldUts.getText().trim());
        double uas = Double.parseDouble(jTextFieldUas.getText().trim());
        
        java.util.List<model.Mahasiswa> listMhs = mahasiswaController.getMahasiswa("", 1, 100);
        java.util.List<model.Course> listCourse = courseController.getCourse("", 1, 100);
        java.util.List<model.Lecturer> listLecturer = lecturerController.getLecturer("", 1, 100);
        
        int idMahasiswaTerpilih = listMhs.get(indeksMhs).getId(); 
        int idCourseTerpilih = listCourse.get(indeksCourse).getId();       
        int idLecturerTerpilih = listLecturer.get(indeksLecturer).getId(); 
        int semesterTerpilih = Integer.parseInt(jComboBoxSemester.getSelectedItem().toString());
        
        model.Krs krsBaru = new model.Krs();
        krsBaru.setIdMahasiswa(idMahasiswaTerpilih);
        krsBaru.setIdCourse(idCourseTerpilih);
        krsBaru.setIdLecturer(idLecturerTerpilih);
        krsBaru.setSemester(semesterTerpilih);
        krsBaru.setNilaiSikap(sikap);
        krsBaru.setNilaiUts(uts);
        krsBaru.setNilaiUas(uas); 
        
        if (krsController.create(krsBaru) > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Transaksi KRS berhasil ditambahkan!");
            loadData(""); // Refresh JTable visual agar langsung menampilkan grade huruf baru
            
            jTextFieldSikap.setText("");
            jTextFieldUts.setText("");
            jTextFieldUas.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menambahkan transaksi KRS.");
        }
        
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Format nilai harus berupa angka yang valid!");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

