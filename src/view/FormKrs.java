    private String hitungGradeHuruf(double sikap, double uts, double uas) {
    // 1. Hitung Nilai Akhir berdasarkan bobot
    double nilaiAkhir = (0.30 * sikap) + (0.35 * uts) + (0.35 * uas);
    
    // 2. Konversi ke Huruf
    if (nilaiAkhir >= 80) {
        return "A";
    } else if (nilaiAkhir >= 70) {
        return "B";
    } else if (nilaiAkhir >= 60) {
        return "C";
    } else if (nilaiAkhir >= 50) {
        return "D";
    } else {
        return "E";
    }
}
    
