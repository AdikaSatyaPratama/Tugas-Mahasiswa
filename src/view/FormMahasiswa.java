private void loadData (String kataKunci){
    String[] kolom = {"ID", "NIM" , "Nama Mahasiswa", "Jurusan"};
    javax.swing.table.DefaultTableModel modelTabel = new javax.swing.table.DefaultTableModel(kolom, 0);
    
    // Memanggil via controller
    java.util.List<model.Mahasiswa> listMhs = mhsController.getMahasiswa(kataKunci, halamanSekarang, jumlahPerHalaman);
    
    for (model.Mahasiswa mhs : listMhs) {
        
        Object[] baris = {mhs.getId(), mhs.getNim(), mhs.getNamaMahasiswa(), mhs.getJurusan()};
        modelTabel.addRow(baris);
    }
    jTableMahasiswa.setModel(modelTabel);
    
    jLabelHalaman.setText("Halaman " + halamanSekarang);
}
