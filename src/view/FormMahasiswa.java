    private void jButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahActionPerformed
         // TODO add your handling code here:                                            
    String nim = jTextFieldNim.getText().trim();
    String nama = jTextFieldNama.getText().trim();
    String jurusan = jComboBoxJurusan.getSelectedItem().toString();
    
    if (nim.isEmpty() || nama.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "NIM dan Nama tidak boleh kosong!");
        return;
    }
    model.Mahasiswa mhsBaru = new model.Mahasiswa(0, nim, nama, jurusan);
    
    // GANTI DI SINI: menggunakan mhsController.create
    if (mhsController.create(mhsBaru) > 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Data Mahasiswa berhasil ditambah");
        loadData("");
        
        jTextFieldNim.setText("");
        jTextFieldNama.setText("");
        jComboBoxJurusan.setSelectedIndex(0);
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menambah data.");
    }
    }//GEN-LAST:event_jButtonTambahActionPerformed

