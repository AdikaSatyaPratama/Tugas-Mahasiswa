    private void jTableMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMahasiswaMouseClicked
        // TODO add your handling code here:
int row = jTableMahasiswa.getSelectedRow();
    if (row != -1) {
        
        idMahasiswaTerpilih = Integer.parseInt(jTableMahasiswa.getValueAt(row, 0).toString());
       
        jTextFieldNim.setText(jTableMahasiswa.getValueAt(row, 1).toString());
        jTextFieldNama.setText(jTableMahasiswa.getValueAt(row, 2).toString());
        jComboBoxJurusan.setSelectedItem(jTableMahasiswa.getValueAt(row, 3).toString());
    }
    }//GEN-LAST:event_jTableMahasiswaMouseClicked

    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:                                          
    String nim = jTextFieldNim.getText().trim();
    String nama = jTextFieldNama.getText().trim();
    String jurusan = jComboBoxJurusan.getSelectedItem().toString();

    if (nim.isEmpty() || nama.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih data pada tabel terlebih dahulu!");
        return;
    }

    model.Mahasiswa mhsEdit = new model.Mahasiswa(idMahasiswaTerpilih, nim, nama, jurusan);
   
    if (mhsController.update(mhsEdit) > 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Data Mahasiswa Berhasil Diubah!");
        loadData(""); // Refresh tabel
        jTextFieldNim.setEditable(true); 
        jTextFieldNim.setText("");
        jTextFieldNama.setText("");
        jComboBoxJurusan.setSelectedIndex(0);
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengubah data.");
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
        // TODO add your handling code here:                                           
    String nim = jTextFieldNim.getText().trim();

    if (nim.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih data pada tabel yang ingin dihapus!");
        return;
    }

    int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus NIM " + nim + "?", "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);
    if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
        
        
        if (mhsController.delete(idMahasiswaTerpilih) > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data Mahasiswa Berhasil Dihapus!");
            loadData(""); 
            jTextFieldNim.setEditable(true);
            jTextFieldNim.setText("");
            jTextFieldNama.setText("");
            jComboBoxJurusan.setSelectedIndex(0);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus data.");
        }
    }

    }//GEN-LAST:event_jButtonHapusActionPerformed

